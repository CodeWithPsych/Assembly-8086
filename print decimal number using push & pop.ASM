.MODEL SMALL
.STACK 100H
.DATA
.CODE
MAIN PROC
MOV AX,@DATA
MOV DS,AX
    XOR CX,CX
    MOV AX,1234
    MOV BH,10
PUSH_TO_STACK:

    DIV BH
    PUSH AX
    INC CX
    XOR AH,AH
    CMP AL,10
    JAE PUSH_TO_STACK

    POP BX
    MOV DL,BL
    ADD DL,'0'
    MOV AH,02
    INT 21H
    MOV DL,BH
    ADD DL,'0'
    INT 21H
    DEC CX
    JZ EXIT

POP_AND_DISPLAY:
    POP BX
    MOV DL,BH
    ADD DL,'0'
    MOV AH,02
    INT 21H
    LOOP POP_AND_DISPLAY
    
    



EXIT:
MOV AH,4CH
INT 21H
MAIN ENDP
END MAIN
