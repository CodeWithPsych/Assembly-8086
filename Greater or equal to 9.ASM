.MODEL SMALL
.STACK 100H
.DATA
IF_MSG DB 'THE VALUE IS GREATER OR EQUAL TO 9$'
ELSE_MSG DB 'THE VALUE IS NOT GREATER THAN 9$'
.CODE
MAIN PROC
    MOV AX,@DATA
    MOV DS,AX

    MOV AH,01
    INT 21H

    CMP AL,'9'
    JAE PRINT

    MOV AH,09
    MOV DX,OFFSET ELSE_MSG
    INT 21H
    JMP EXIT

PRINT:
    MOV AH,09
    MOV DX,OFFSET IF_MSG
    INT 21H

EXIT:
    MOV AH,4CH
    INT 21H

MAIN ENDP
END MAIN
