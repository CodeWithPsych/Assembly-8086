.MODEL SMALL
.STACK 100H
.DATA
MSG1 DB "THE INPUT IS A $"
MSG2 DB "THE INPUT IS NOT A$"

.CODE
MAIN PROC
    MOV AX,@DATA
    MOV DS,AX

    MOV AH,01
    INT 21H

    SUB AL,'A'
    JZ PRINT_MSG1

    MOV AH,09
    MOV DX,OFFSET MSG2
    INT 21H
    JMP EXIT

PRINT_MSG1:
    MOV AH,09
    MOV DX,OFFSET MSG1
    INT 21H

EXIT:
    MOV AH,4CH
    INT 21H
    

    MOV AH,4CH
    INT 21H

MAIN ENDP
END MAIN
