.MODEL SMALL
.STACK 100H
.DATA
.CODE
MAIN PROC
    MOV AX,@DATA
    MOV DS, AX
    MOV AX, 1723
    CALL DEC_OUTPUT

EXIT:
    MOV AH,4CH
    INT 21H
MAIN ENDP
DEC_OUTPUT PROC
    PUSH AX
    PUSH BX
    PUSH CX
    PUSH DX

    ;THIS PROCEDURE DISPLAY THE VALUE IN AX
    XOR CX,CX
    MOV BH,10
DIVIDE_AGAIN:
    DIV BH  
    PUSH AX
    INC CX
    XOR AH,AH
    CMP AL,00
    JNZ DIVIDE_AGAIN
    mov ah,02
DISPLAY:
    POP DX
    MOV DL,DH
    ADD DL,'0'
    INT 21H
    LOOP DISPLAY

    POP DX
    POP CX
    POP BX
    POP AX
RET
DEC_OUTPUT ENDP
END MAIN
