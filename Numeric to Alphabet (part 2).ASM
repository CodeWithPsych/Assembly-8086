.MODEL SMALL
.STACK 100H
.DATA
.CODE
MAIN PROC
    MOV AX,@DATA
    MOV DS,AX
    MOV CL,03
;NIPUT THREE CHARACTERS
INPUT_AGAIN:
   MOV AH,01
   INT 21H

   MOV BL,AL
   SUB BL,30H
   ADD BL,41H

   MOV AH,02
   MOV DL,0AH
   INT 21H

   MOV DL,BL
   INT 21H
   MOV DL,10
   MOV AH,02

   INT 21H

   DEC CL
    
   JNZ INPUT_AGAIN
    MOV AH,4CH
    INT 21H
MAIN ENDP
END MAIN

