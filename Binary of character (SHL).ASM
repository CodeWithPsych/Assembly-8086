.MODEL SMALL
.STACK 100H
.DATA
NAME_Y DB ?
NAME_N DB ?

.CODE
MAIN PROC
    MOV AX,@DATA
    MOV DS,AX
   ;INPUT A CHARACTER
    MOV AH,01
    INT 21H
    LEA SI,NAME_Y
    MOV [SI],AL
    ;SET LOOP COUNTER VALUE
    MOV CH,08
    MOV BH,[SI]

BINARY:
    SHL BH,1
    MOV AH,02
    JC PRINT_ONE
    MOV DL,'0'
    INT 21H
    DEC CH
    JNZ BINARY
    JMP EXIT
PRINT_ONE:
    MOV DL,'1'
    INT 21H
    DEC CH
    JNZ BINARY

EXIT:
    MOV AH,4CH
    INT 21H

MAIN ENDP
END MAIN
