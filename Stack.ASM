.MODEL SMALL
.STACK 100H
.DATA
MESSAGE DB "HY ITS COAL $"
.CODE
MAIN PROC
    MOV AX,@DATA
    MOV DS,AX
    LEA DX,MESSAGE
    MOV AH,09
    INT 21H
    CALL NEWLINE
    MOV AH,01
    INT 21H
    CALL NEWLINE
    CALL NEWLINE
    MOV DL,AL
    MOV AH,02
    INT 21H
    
    MOV AH,4CH
    INT 21H
MAIN ENDP
NEWLINE PROC
PUSH AX
PUSH BX
PUSH CX

MOV AH,02
MOV DL,10
INT 21H

POP CX
POP BX
POP AX
RET
NEWLINE ENDP
END MAIN








