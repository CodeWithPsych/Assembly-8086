.MODEL SMALL
.STACK 100H
.DATA
MSG1 DB 'TAKE A SINGLE CHARACTER INPUT$'
MSG2 DB 'INPUT CHARCTER IS $'

.CODE
MAIN PROC
    MOV AX,@DATA
    MOV DS,AX
    MOV DX,OFFSET MSG1
    MOV AH,09
    INT 21H
    MOV DL,10
    MOV AH,02
    INT 21H

    ;single character OUTPUT
    MOV AH,01
    INT 21H

    MOV AH,4CH
    INT 21H
MAIN ENDP
END MAIN
