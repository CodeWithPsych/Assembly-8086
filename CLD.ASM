.MODEL SMALL
.STACK 100H
.DATA
;STRING OPERATIONS,movsb,LODSB,STOSB
;LOAD:SOURCE-> MEMORY, DESTINATION->REGISTER 
;STORE:1- SOURCE->REGISTER(AL), DESTINATION->MEMORY (ARRAY/STRING) (2-OFFSET WILL BE HELD BY ES:DI)
STR1 DB "ABCDEF$"
STR2 DB "XYZZXY$"
.CODE
MAIN PROC
    MOV AX,@DATA
    MOV DS,AX
    MOV ES,AX

    ;FIRST COMPLETE THE REQUIREMENTS FOR STOSB
    MOV AL,62H ; INPUT 
    LEA DI,STR1
    LEA SI,STR2

    CLD ;DF=0
    MOVSB ;one character pointed by SI will be moved to place pointed by DI. 
    ;DI,SI WILL BE INCREMENTED AFTER THE EXECUTION OF STOSB
    ;DISPLAY THE UPDATED STRING
    MOV AH,09
    LEA DX,STR2
    INT 21H

    MOV AH,4CH
    INT 21H
MAIN ENDP
END MAIN
